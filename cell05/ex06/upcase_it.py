#!/usr/bin/env python3
import sys

# 1. ตรวจสอบจำนวนพารามิเตอร์
# sys.argv มีค่าเท่ากับ 2 หมายถึงมีชื่อไฟล์ (0) และพารามิเตอร์ 1 ตัว (1)
if len(sys.argv) == 2:
    # 2. นำพารามิเตอร์ตัวแรกมาแปลงเป็นตัวพิมพ์ใหญ่และแสดงผล
    print(sys.argv[1].upper())
else:
    # 3. หากไม่มีพารามิเตอร์ หรือมีมากกว่า 1 ให้แสดง none
    print("none")
